// Verificar se aluno passou de ano
// Requisitos: nota >= 6, presença >= 75%
// Operador lógico e (and) em javascript: &&, se as duas condições forem verdades o resultado é true
let notaAluno = 5;
let presencaAluno = 80;
let resultado =
  notaAluno >= 6 && presencaAluno >= 75 ? "Aprovado" : "Reprovado";
