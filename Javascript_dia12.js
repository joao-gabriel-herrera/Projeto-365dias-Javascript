// Projeto código nosso de cada dia (dia 12)
// Laço de repetição usando While
let i = 1;
while (i <= 5) {
  console.log("Estou na minha ", i, "º repetição!");
  i++;
}
// Laço de repetição com Do...While
let i2 = 6;
do {
  console.log("Estou na minha ", i2, "º repetição!");
  i2++;
} while (i2 <= 10);
